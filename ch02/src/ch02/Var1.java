public class Var1 {
	public static void main(String[] args) {
		int i1 = 12;
		int i2 = 45;
		System.out.printf("%d + %d = %d\n", i1, i2, i1 + i2);
		System.out.printf("%d - %d = %d\n", i1, i2, i1 - i2);
		System.out.printf("%d * %d = %d\n", i1, i2, i1 * i2);
		// 자바에서 정수끼리 연사은 결과도 정수
		System.out.printf("%d / %d = %d\n", i1, i2, i1 / i2);
	}
}
