package ch01;

public class Print2 {
	public static void main(String[] args) {
		int x = 87;
		System.out.println("문자열");
		System.out.println(100);
		System.out.printf("%d\n", 122);
		System.out.printf("%d\n", x);
		System.out.printf("%s\n", "수요일");
		System.out.printf("PI는 %s", "3.14 입니다.\n");
		System.out.println(1500 + "원 입니다.");
		System.out.printf("%s: %d단\n", "구구단", 5);
		System.out.println("\n3 + 5 = " + 8); // 문자열
		System.out.println(3 + 5 + " = " + 8); // 덧셈
	}
}
